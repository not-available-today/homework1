package com.company;

public class Hw16 {
    public static void main(String[] args) {
        double radius, perimeter, area;

        radius = 8.0;
        perimeter = 2 * Math.PI * radius;
        area = Math.PI * radius * radius;

        System.out.println("Perimeter: " + perimeter);
        System.out.println("Area: " + area);
    }
}
